package Branchingstatement;

public class SwitchDemo {

	public static void main(String[] args) {
		
		int statusCode=5;
		
		switch(statusCode)
		{
		case 1:
			System.out.println("Status code =" + statusCode);
			break;
			
		case 2:
			System.out.println("Status code =" + statusCode);
			break;
			
		case 3:
		System.out.println("Status code =" + statusCode);
		break;
		
		case 4:
		System.out.println("Status code =" + statusCode);
		break;
		
		case 5:
		System.out.println("Status code =" + statusCode);
		break;
		
		default:
			System.out.println("Invalid Status code");
		}
	}

}
